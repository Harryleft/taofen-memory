# 自动推断类别的函数
def infer_category(name):
    # 直接匹配名称
    for category, items in categories.items():
        if name in items:
            return category

    # 通过关键词推断
    if any(keyword in name for keyword in ["题词", "书", "题"]):
        return "题词"
    elif any(keyword in name for keyword in ["致", "信", "简覆", "函"]):
        return "书简"
    else:
        return "文稿"  # 默认为文稿

# 主函数
def add_tags():
    # 文件路径
    json_path = r"S:\vibe_coding\taofen_web\frontend\public\data\json\taofen_handwriting_details.json"

    # 读取JSON文件
    with open(json_path, 'r', encoding='utf-8') as file:
        data = json.load(file)

    # 添加标签
    for item in data:
        name = item.get("名称", "")
        item["标签"] = infer_category(name)

    # 写回JSON文件
    with open(json_path, 'w', encoding='utf-8') as file:
        json.dump(data, file, ensure_ascii=False, indent=2)

    print(f"已为 {len(data)} 条记录添加标签")

if __name__ == "__main__":
    add_tags()
